SELECT 
	A.DCODE
	,F.DNAME
	,C.DCODE A 
FROM
RXBackend.dbo.FAC_ALT A 
LEFT JOIN CIPS.dbo.FAC F 
	ON LTRIM(RTRIM(A.DCODE)) = F.DCODE
LEFT JOIN CIPS.dbo.FAC_CHG G 
	ON F.ID = G.FAC_ID
LEFT JOIN CIPS.dbo.CHG C
	ON G.CHG_ID = C.ID
WHERE C.DCODE IS NOT NULL
--ORDER BY C.DCODE DESC
ORDER BY A.DCODE

SELECT 
A.DCODE
,F.DNAME
,ISNULL(
STUFF((SELECT '; ' + C.DCODE
          FROM
			CIPS.dbo.FAC FS 
			LEFT JOIN CIPS.dbo.FAC_CHG G 
				ON FS.ID = G.FAC_ID
			LEFT JOIN CIPS.dbo.CHG C
				ON G.CHG_ID = C.ID
          WHERE A.DCODE = FS.DCODE
          FOR XML PATH('')), 1, 1, ''), '') [Accounts]
,ISNULL(
STUFF((SELECT '; ' + ADDRESS
          FROM
			FAC_EMAIL E
          WHERE A.DCODE = E.FAC_CODE AND Billing = 1
          FOR XML PATH('')), 1, 1, ''), '') [Email]
, '' as Documents
FROM
RXBackend.dbo.FAC_ALT A 
LEFT JOIN CIPS.dbo.FAC F 
	ON A.DCODE = F.DCODE
--LEFT JOIN CIPS.dbo.FAC_CHG G 
--	ON F.ID = G.FAC_ID
--LEFT JOIN CIPS.dbo.CHG C
--	ON G.CHG_ID = C.ID

update FAC_EMAIL set billing = 1

SELECT 
   SS.SEC_NAME,
   STUFF((SELECT '; ' + US.USR_NAME 
          FROM USRS US
          WHERE US.SEC_ID = SS.SEC_ID
          FOR XML PATH('')), 1, 1, '') [SECTORS/USERS]
FROM SALES_SECTORS SS
GROUP BY SS.SEC_ID, SS.SEC_NAME
ORDER BY 1


